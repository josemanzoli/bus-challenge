package com.manzoli.bus.route.domain;

import org.springframework.data.neo4j.annotation.EndNode;
import org.springframework.data.neo4j.annotation.Fetch;
import org.springframework.data.neo4j.annotation.RelationshipEntity;
import org.springframework.data.neo4j.annotation.StartNode;

/**
 * 
 * @author jmanzol
 * @since 1.0-SNAPSHOT Relationship Entity for the nodes Route and Station
 *
 */
@RelationshipEntity(type = "DIRECT")
public class DirectRoute {

	@StartNode
	@Fetch
	private Route from;

	@EndNode
	@Fetch
	private Station to;

	public Route getFrom() {
		return from;
	}

	public void setFrom(Route from) {
		this.from = from;
	}

	public Station getTo() {
		return to;
	}

	public void setTo(Station to) {
		this.to = to;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((from == null) ? 0 : from.hashCode());
		result = prime * result + ((to == null) ? 0 : to.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DirectRoute other = (DirectRoute) obj;
		if (from == null) {
			if (other.from != null)
				return false;
		} else if (!from.equals(other.from))
			return false;
		if (to == null) {
			if (other.to != null)
				return false;
		} else if (!to.equals(other.to))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "DirectRoute [from=" + from + ", to=" + to + "]";
	}
}
